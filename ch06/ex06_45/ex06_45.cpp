#include <iostream>
#include <cmath>

int main() {
	std::cout << "ceil(x)\trounds x to the smallest integer not less than x" << std::endl;
	std::cout << "ceil(9.2) is " << ceil(9.2) << std::endl;
	std::cout << "ceil(-9.8) is " << ceil(-9.8) << std::endl;

	std::cout << "\ncos(x)\ttrigonometric cosine of x (x in radians)" << std::endl;
	std::cout << "cos(0.0) is " << cos(0.0) << std::endl;

	std::cout << "\nexp(x)\texponential function e^x" << std::endl;
	std::cout << "exp(1.0) is " << exp(1.0) << std::endl;
	std::cout << "exp(2.0) is " << exp(2.0) << std::endl;

	std::cout << "\nfabs(x)\tabsolute value of x" << std::endl;
	std::cout << "fabs(5.1) is " << fabs(5.1) << std::endl;
	std::cout << "fabs(0.0) is " << fabs(0.0) << std::endl;
	std::cout << "fabs(-8.76) is " << fabs(-8.76) << std::endl;

	std::cout << "\nfloor(x)\trounds x to the largest integer not greater than x" << std::endl;
	std::cout << "floor(9.2) is " << floor(9.2) << std::endl;
	std::cout << "floor(-9.8) is " << floor(-9.8) << std::endl;

	std::cout << "\nfmod(x, y)\tremainder of x/y as a floating-point number" << std::endl;
	std::cout << "fmod(2.6, 1.2) is " << fmod(2.6, 1.2) << std::endl;

	std::cout << "\nlog(x)\tlogarithm of x (base e)" << std::endl;
	std::cout << "log(2.718282) is " << log(2.718282) << std::endl;
	std::cout << "log(7.389056) is " << log(7.389056) << std::endl;

	std::cout << "\nlog10(x)\tlogarithm of x base(10)" << std::endl;
	std::cout << "log10(10.0) is " << log10(10.0) << std::endl;
	std::cout << "log10(100.0) is " << log10(100.0) << std::endl;

	std::cout << "\npow(x, y)\tx raised to power y (x^y)" << std::endl;
	std::cout << "pow(2, 7) is " << pow(2, 7) << std::endl;
	std::cout << "pow(9, .5) is " << pow(9, .5) << std::endl;

	std::cout << "\nsin(x)\ttrigonometric sine of x (x in radians)" << std::endl;
	std::cout << "sin(0.0) is " << sin(0.0) << std::endl;

	std::cout << "\nsqrt(x)\tsquare root of x (where x is a nonnegative value)" << std::endl;
	std::cout << "sqrt(9.0) is " << sqrt(9.0) << std::endl;

	std::cout << "\ntan(x)\ttrigonometric tangent of x (x in radians)" << std::endl;
	std::cout << "tan(0.0) is " << tan(0.0) << std::endl;





}